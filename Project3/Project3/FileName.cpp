#include <stdio.h>

// -------------------------------------------
// [문제 3] C언어에서 함수를 사용하는 이유
// -------------------------------------------
// 1. 코드의 재사용성 향상 : 한 번 만든 함수를 여러 번 호출 가능
// 2. 프로그램의 가독성 향상 : 기능별로 구분되어 구조가 명확
// 3. 유지보수 용이성 : 함수 단위로 수정 및 관리가 쉬움
// 4. 디버깅이 쉬움 : 문제 발생 시 해당 함수만 점검하면 됨
// -------------------------------------------


// 함수 선언 (프로토타입)
int add(int a, int b);
// → add 함수는 두 정수를 받아서 그 합을 반환하는 역할을 함


int main(void) {
    int num1, num2, result;  // 변수 선언

    printf("두 개의 정수를 입력하세요: ");
    scanf("%d %d", &num1, &num2);  // 사용자로부터 두 정수 입력 받기

    result = add(num1, num2);  // add 함수를 호출하여 결과를 받음

    printf("두 수의 합은 %d입니다.\n", result);  // 결과 출력

    return 0;  // 프로그램 정상 종료
}


// 함수 정의 부분
int add(int a, int b) {
    // 두 정수를 더한 값을 반환
    return a + b;
}


